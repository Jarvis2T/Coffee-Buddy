			<h1>Coffea Arabica — C. Arabica</h1>
			<p>Varieties: Bourbon, Typica, Caturra, Mundo Novo, Tico, San Ramon, Jamaican Blue Mountain</p>
			<p>Coffea Arabica is descended from the original coffee trees discovered in Ethiopia. These trees produce a fine, mild, aromatic coffee and represent approximately 70% of the world's coffee production. The beans are flatter and more elongated than Robusta and lower in caffeine.</p>
			<p>On the world market, Arabica coffees bring the highest prices.  The better Arabicas are high grown coffees — generally grown between 2,000 to 6,000 feet (610 to 1830 meters) above sea level — though optimal altitude varies with proximity to the equator.</p>
			<p>The most important factor is that temperatures must remain mild, ideally between 59 - 75 degrees Fahrenheit, with about 60 inches of rainfall a year. The trees are hearty, but a heavy frost will kill them.</p>
			<span>Arabica trees are costly to cultivate because the ideal terrain tends to be steep and access is difficult. Also, because the trees are more disease-prone than Robusta, they require additional care and attention.</span>
