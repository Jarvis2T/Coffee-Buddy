			<h1>Coffea canephora — C. canephora</h1>
			<p>Variety: Robusta</p>
			<p>Most of the world's Robusta is grown in Central and Western Africa, parts of Southeast Asia, including Indonesia and Vietnam, and in Brazil. Production of Robusta is increasing, though it accounts for only about 30% of the world market.</p>
			<p>Robusta is primarily used in blends and for instant coffees. The Robusta bean itself tends to be slightly rounder and smaller than an Arabica bean.</p>
			<p>The Robusta tree is heartier and more resistant to disease and parasites, which makes it easier and cheaper to cultivate. It also has the advantage of being able to withstand warmer climates, preferring constant temperatures between 75 and 85 degrees Fahrenheit, which enables it to grow at far lower altitudes than Arabica.</p>
			<span>It requires about 60 inches of rainfall a year, and cannot withstand frost. Compared with Arabica, Robusta beans produce a coffee which has a distinctive taste and about 50-60% more caffeine.</span>
